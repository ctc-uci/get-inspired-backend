    rakerName VARCHAR,
    startLat DECIMAL,
    startLong DECIMAL,
    startTime DATETIME,
    endTime DATETIME,
    endLat DECIMAL,
    endLong DECIMAL,
    startDepth DECIMAL,
    endDepth DECIMAL,
    startSlope DECIMAL,
    endSlope DECIMAL,
    rakeArea VARCHAR,
};

